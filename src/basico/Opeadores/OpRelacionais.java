package basico.Opeadores;

public class OpRelacionais {

    public static void main(String[] args) {

        int valor1 = 1;
        int valor2 = 2;

        System.out.println("valor1 == valor2: " + (valor1 == valor2));
        //igual
        System.out.println("valor1 != valor2: " + (valor1 != valor2));
        //diferente de
        System.out.println("valor1 > valor2: " + (valor1 > valor2));
        //maior que
        System.out.println("valor1 < valor2: " + (valor1 < valor2));
        //menor que
        System.out.println("valor1 >= valor2: " + (valor1 >= valor2));
        //maior ou igual que
        System.out.println("valor1 <= valor2: " + (valor1 <= valor2));
        //menor ou igual que

    }

}
